//while é utilizado quando o nº de repetições não é conhecido previamente
//Equanto a condição for verdadeira, ira imprimir os numeros 
public class ExemploWhile {
    public static void main (String [] args){
        int i =1;
        while (i <=5){
            System.out.println("Numero"+i);
            i++;
        }
    }
}